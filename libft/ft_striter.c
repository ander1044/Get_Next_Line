#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char	*str;
	int		i;

	str = (char *)s;
	i = 0;
	if (s == NULL || f == NULL)
		return ;
	while (str[i])
	{
		(f)(&str[i]);
		i++;
	}
}
