#include "libft.h"

void	ft_concasei(unsigned int n, char *ch)
{
	(void)n;
	if (*ch == 0)
		return ;
	if (*ch >= 'a' && *ch <= 'z')
		*ch = *ch - 32;
	else if (*ch >= 'A' && *ch <= 'Z')
		*ch = *ch + 32;
}
