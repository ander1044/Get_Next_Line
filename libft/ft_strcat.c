#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*s1;
	char	*s2;

	s1 = dest;
	s2 = (char *)src;
	ft_strcpy(s1 + ft_strlen(s1), s2);
	return (s1);
}
