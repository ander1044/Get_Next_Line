#include "libft.h"

void	ft_strclr(char *s)
{
	char	*str;
	int		i;

	i = 0;
	str = (char *)s;
	if (str == NULL)
		return ;
	while (str[i])
	{
		s[i] = '\0';
		i++;
	}
}
